#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	int		i;
	char	*temp;

	i = 0;
	temp = str;
	while (n && temp[i])
	{
		temp[i] = c;
		n--;
		i++;
	}
	return (temp);
}
